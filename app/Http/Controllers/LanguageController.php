    public function store(Request $request)
    {
        $validator = Validator::make($request->all('name'),
            [
                'name' => 'required|unique:languages,name',
            ],
            [
                'name.required' => 'لطفا عنوان را وارد کنید',
                'name.unique' => 'این عنوان قبلا ثبت شده است',
            ]
        );
        if ($validator->fails()) {
            return response()->json($validator->messages(), 422);
        }
        try {
//            return $request['sizes'];
            $language = Language::create($request->all());
            return response(new LanguageResource($language), 201);
        } catch (\Exception $exception) {
            return response($exception);
        }
    }

    public function update(Request $request, Language $language)
    {
        $validator = Validator::make($request->all('name'),
            [
                'name' => 'required|unique:languages,name,' . $language['id'],
            ],
            [
                'name.required' => 'لطفا عنوان را وارد کنید',
                'name.unique' => 'این عنوان قبلا ثبت شده است',
            ]
        );
        if ($validator->fails()) {
            return response()->json($validator->messages(), 422);
        }
        try {
            $language->update($request->all());
            return response(new LanguageResource($language), 200);
        } catch (\Exception $exception) {
            return response($exception);
        }
    }

